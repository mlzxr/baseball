    //计算图片缩放值
    public static int picZoomSize(BitmapFactory.Options options, int reqWidth, int reqHeight) {
        //获取图片大小
        final int width = options.outWidth;
        final int height = options.outHeight;
        int inSampleSize = 1;
        if (width > reqWidth || height > reqHeight) {
            final int halfHeight = height / 2;
            final int halfWidth = width / 2;

            while ((halfHeight / inSampleSize) >= reqHeight && (halfWidth / inSampleSize) >= reqWidth) {
                inSampleSize *= 2;
            }
        }
        return inSampleSize;
    }

    //根据图片获取路径并且压缩
    public static Bitmap getSmallBitmap(String path, int width, int height) {
        //
        final BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        BitmapFactory.decodeFile(path, options);
        options.inSampleSize = picZoomSize(options, width, height);
        options.inJustDecodeBounds = false;
        //
        Bitmap bm = BitmapFactory.decodeFile(path, options);
        if (bm == null) {
            return null;
        }
        int degree = readPictureDegree(path);
        bm = rotateBitmap(bm, degree);

        /**
        bm = compressImage(Bitmap.createScaledBitmap(bm, width, height, false));
        */

        ByteArrayOutputStream baos = null;
        int ratio = 60;
        try {
            baos = new ByteArrayOutputStream();
            bm.compress(Bitmap.CompressFormat.JPEG, ratio, baos);

            byte[] bitmapData = baos.toByteArray();
            //
            bm = BitmapFactory.decodeByteArray(bitmapData, 0, bitmapData.length);
            return bm;
        } finally {
            try {
                if (baos != null)
                    baos.close();
            } catch (IOException e) {
                return null;
            }
        }
    }

    //质量压缩
    private static Bitmap compressImage(Bitmap bitmap) {
        ByteArrayOutputStream baos = null;// 把压缩后的数据baos存放到ByteArrayInputStream中
        try {
            baos = new ByteArrayOutputStream();
            bitmap.compress(Bitmap.CompressFormat.JPEG, 100, baos);// 质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
            int options = 100;
            while (baos.toByteArray().length / 1024 > 100) { // 循环判断如果压缩后图片是否大于100kb,大于继续压缩
                options -= 10;// 每次都减少10
                baos.reset();// 重置baos即清空baos
                bitmap.compress(Bitmap.CompressFormat.JPEG, options, baos);// 这里压缩options%，把压缩后的数据存放到baos中
            }
            byte[] bitmapData = baos.toByteArray();
            return BitmapFactory.decodeByteArray(bitmapData, 0, bitmapData.length);
        } finally {
            try {
                if (baos != null)
                    baos.close();
            } catch (IOException e) {
                return null;
            }
        }
    }


    private static Bitmap rotateBitmap(Bitmap bitmap, int rotate) {
        if (bitmap == null)
            return null;
        int w = bitmap.getWidth();
        int h = bitmap.getHeight();
        Matrix mtx = new Matrix();
        mtx.postRotate(rotate);
        return Bitmap.createBitmap(bitmap, 0, 0, w, h, mtx, true);
    }

    //处理图片旋转
    private static int readPictureDegree(String path) {
        int degree = 0;
        try {
            ExifInterface exifInterface = new ExifInterface(path);
            int orientation = exifInterface.getAttributeInt(ExifInterface.TAG_ORIENTATION, ExifInterface.ORIENTATION_NORMAL);
            switch (orientation) {
                case ExifInterface.ORIENTATION_ROTATE_90:
                    degree = 90;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_180:
                    degree = 180;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_270:
                    degree = 270;
                    break;
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return degree;
    }

